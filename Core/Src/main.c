/* USER CODE BEGIN 0 */
void Generate_Sine_LUT(void)
{
    const uint32_t PERIOD = 3333;               // Timer Period para 15 kHz PWM (center aligned)
    const uint32_t CENTER = PERIOD / 2;         // Valor central (50% duty)
    const uint32_t AMPLITUDE = (uint32_t)(CENTER * 0.9f);  // 90% amplitude para evitar saturação

    for (int i = 0; i < CCRValue_BufferSize; i++)
    {
        float angle = (2.0f * M_PI * i) / CCRValue_BufferSize;
        float sine_value = sinf(angle);

        int32_t val = CENTER + (int32_t)(AMPLITUDE * sine_value);

        // Limites mínimos e máximos para evitar under/overflow do CCR
        if (val < 10)
            val = 10;
        else if (val >= (int32_t)PERIOD)
            val = PERIOD - 1;

        DiscontinuousSineCCRValue_Buffer[i] = (uint32_t)val;
    }
}
